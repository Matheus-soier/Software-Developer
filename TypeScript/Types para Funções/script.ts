type mathFunction = (n1: number, n2: number) => number;

const somar: mathFunction = (n1, n2) => {
    return n1 + n2;
}

const subtrair: mathFunction = (n1, n2) => {
    return n1 - n2;
}

const multiplicar: mathFunction = (n1, n2) => {
    return n1 * n2;
}

const dividir: mathFunction = (n1, n2) => {
    return n1 / n2;
}
