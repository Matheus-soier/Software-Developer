/*Tipos de variáveis:

let -> pode ter o valor modificado com o tempo
const -> NÃo pode ter o valor modificado com o tempo
var -> pode ter o valor modificado com o tempo

*/

// number
let idade = 20
// stringer
let nome = "Matheus Soier"
// stringer
let cidade = "São Paulo"
// boolean
let logado = true
// arrays
let receita = ["ovo", "açucar", "massa", "corante", "morango"]
// object
let nomeCompleto = { nome: "Matheus Soier", idade: 20 }

// imprimir no console
console.log(nome)
console.log(idade)
console.log(cidade)
console.log(logado)
console.log(receita)
console.log(nomeCompleto)

// imprimir type da variavel
console.log(typeof nome)
console.log(typeof idade)
console.log(typeof cidade)
console.log(typeof logado)
console.log(typeof receita)
console.log(typeof nomeCompleto)

